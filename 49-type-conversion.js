// type coercing

var s = "14";
var n = Number(s);
document.write(typeof s, "<br>");
document.write(typeof n, "<br>");

var x = 3.14;
var y = String(x);
document.write(typeof x, "<br>");
document.write(typeof y, "<br>");

var a = Number();
document.write(a, '<br>');
document.write(typeof a, '<br>');

var b;
document.write(b, '<br>');
document.write(typeof b, '<br>');


